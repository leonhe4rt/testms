package com.capgemini.captivams.beans;

public class DatiScansione {

	private String idUsername;
	private String idTemplate;
	private String filePDFFattura;
	private String nomeFile;

	public String getIdUsername() {
		return idUsername;
	}

	public void setIdUsername(String idUsername) {
		this.idUsername = idUsername;
	}

	public String getIdTemplate() {
		return idTemplate;
	}

	public void setIdTemplate(String idTemplate) {
		this.idTemplate = idTemplate;
	}

	public String getFilePDFFattura() {
		return filePDFFattura;
	}

	public void setFilePDFFattura(String filePDFFattura) {
		this.filePDFFattura = filePDFFattura;
	}

	public String getNomeFile() {
		return nomeFile;
	}

	public void setNomeFile(String nomeFile) {
		this.nomeFile = nomeFile;
	}

}
